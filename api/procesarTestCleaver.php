function obtenerLetraMayor($d, $i, $s, $c)
{
    $letraMayor = '';

    if ($d >= $i && $d >= $s && $d >= $c) {
        $letraMayor = 'D';
    } elseif ($i >= $d && $i >= $s && $i >= $c) {
        $letraMayor = 'I';
    } elseif ($s >= $d && $s >= $i && $s >= $c) {
        $letraMayor = 'S';
    } else {
        $letraMayor = 'C';
    }

    return $letraMayor;
}

function obtenerLetraMenor($d, $i, $s, $c)
{
    $letraMayor = '';

    if ($d <= $i && $d <= $s && $d <= $c) {
        $letraMayor = 'D';
    } elseif ($i <= $d && $i <= $s && $i <= $c) {
        $letraMayor = 'I';
    } elseif ($s <= $d && $s <= $i && $s <= $c) {
        $letraMayor = 'S';
    } else {
        $letraMayor = 'C';
    }

    return $letraMayor;
}

